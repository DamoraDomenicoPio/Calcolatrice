package calcolatricecmd;
import java.lang.Math.*;

/**
 *
 * @author assun
 */
public class funzioneMatematica {
    
    
    public static double radiceQuadrata(double x) {
        return Math.sqrt(x);
    }
    
    //log_10(a) = log_e(a)/log_e(10)
    public static double logaritmo10(double x) {
        return Math.log(x)/Math.log(10);
    }
}
